// octal and binary literals

// es5
var b1111 = 15;
var o1111 = 585;
var x1111 = 4369;
console.log('b1111: ', b1111);
console.log('o1111: ', o1111);
console.log('x1111: ', x1111);

// es6
var _b1111 = 0b1111;
var _o1111 = 0o1111;
var _x1111 = 0x1111;
console.log('_b1111: ', _b1111);
console.log('_o1111: ', _o1111);
console.log('_x1111: ', _x1111);
